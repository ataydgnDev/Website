var mLk1 = document.getElementById("mLk1");
var mCt1 = document.getElementById("mCt1");

mLk1.addEventListener("click", function(e) {
  e.preventDefault();
  if (mCt1.style.display === "block") {
    mCt1.style.display = "none";
  } else {
    mCt1.style.display = "block";
  }
});
